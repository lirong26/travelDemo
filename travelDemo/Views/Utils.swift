import UIKit

extension UIColor {
    static func pinkColor() -> UIColor {
        return UIColor(red: 233 / 255.0, green: 65 / 255.0, blue: 136 / 255.0, alpha: 1.0)
    }
    static func cyanColor() -> UIColor {
        return UIColor(red: 96 / 255.0, green: 194 / 255.0, blue: 198 / 255.0, alpha: 1.0)
    }
    static func lightCyanColor() -> UIColor {
        return UIColor(red: 192 / 255.0, green: 226 / 255.0, blue: 231 / 255.0, alpha: 1.0)
    }
    static func purpleColor() -> UIColor {
        return UIColor(red: 182 / 255.0, green: 47 / 255.0, blue: 188 / 255.0, alpha: 1.0)
    }
    static func greyColor() -> UIColor {
        return UIColor(red: 242 / 255.0, green: 242 / 255.0, blue: 242 / 255.0, alpha: 1.0)
    }
}
